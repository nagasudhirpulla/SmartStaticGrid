    public static (IEnumerable<User>, int) GetUsers(GridRequest req)
    {
        // In a real app, this would query your database with the filters and sort from the request
        var users = (IEnumerable<User>)_dummyUsers;

        // filter the user as per each of req.Filters
        foreach (var filter in req.Filters)
        {
            if (string.IsNullOrWhiteSpace(filter.Value))
            {
                continue; // Skip empty filters
            }
            if (filter.Key == EmailKey)
            {
                users = users.Where(u => u.Email.StartsWith(filter.Value.Trim(), StringComparison.OrdinalIgnoreCase));
            }
            else if (filter.Key == NameKey)
            {
                users = users.Where(u => u.FullName.StartsWith(filter.Value.Trim(),
                    StringComparison.OrdinalIgnoreCase));
            }
            else if (filter.Key == RoleKey)
            {
                users = users.Where(u => u.Role.StartsWith(filter.Value.Trim(),
                    StringComparison.OrdinalIgnoreCase));
            }
        }

        // sort the user as per req.SortBy and req.SortDescending
        if (!string.IsNullOrWhiteSpace(req.SortBy))
        {
            if (req.SortBy == EmailKey)
            {
                users = req.SortDesc ? users.OrderByDescending(u => u.Email) : users.OrderBy(u => u.Email);
            }
            else if (req.SortBy == NameKey)
            {
                users = req.SortDesc ? users.OrderByDescending(u => u.FullName) : users.OrderBy(u => u.FullName);
            }
            else if (req.SortBy == RoleKey)
            {
                users = req.SortDesc ? users.OrderByDescending(u => u.Role) : users.OrderBy(u => u.Role);
            }
        }

        users = users.Skip((req.Page - 1) * req.PageSize).Take(req.PageSize);

        int totalCount = -1;
        if (req.RequestNewCount)
        {
            totalCount = _dummyUsers.Count;
        }
        return ([.. users], totalCount);
    }
